package javaBasics;

public class EightJavaClass {

    public static void main(String[] args) {
        //while loop
        int i = 1;
        while (i <= 11) {
            System.out.println(i);
            i++;
        }

        //do while
        int s = 5;
        do {
            System.out.println(s);
            s++;
        } while (s <= 30);

        //do while decree
        int t = 9;
        do {
            System.out.println(t);
            t--;
        } while (t >1);
    }
}